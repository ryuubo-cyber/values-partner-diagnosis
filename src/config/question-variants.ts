/**
 * sessionIdをシードにして質問テキストのバリエーションを選択する
 */
export function getQuestionVariant(questionId: string, sessionId: string): string | null {
  const variants = QUESTION_VARIANTS[questionId];
  if (!variants || variants.length === 0) return null;

  // sessionIdからハッシュ値を生成してバリエーションを選択
  let hash = 0;
  const seed = sessionId + questionId;
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) - hash + seed.charCodeAt(i)) | 0;
  }
  const index = Math.abs(hash) % variants.length;
  return variants[index];
}

/**
 * セッションIDからカテゴリ内の質問順序をシャッフルする
 */
export function shuffleQuestionsForSession<T extends { id?: string; questionId?: string }>(
  questions: T[],
  sessionId: string,
  categoryId: string,
): T[] {
  const shuffled = [...questions];
  let hash = 0;
  const seed = sessionId + categoryId + "shuffle";
  for (let i = 0; i < seed.length; i++) {
    hash = ((hash << 5) - hash + seed.charCodeAt(i)) | 0;
  }

  // Fisher-Yates shuffle with seeded random
  for (let i = shuffled.length - 1; i > 0; i--) {
    hash = ((hash << 5) - hash + i) | 0;
    const j = Math.abs(hash) % (i + 1);
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}
